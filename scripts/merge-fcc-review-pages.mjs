function parseWindowAssign(src, varName) {
  return JSON.parse(
    src
      .replace(new RegExp(`^\\s*window\\.${varName}\\s*=\\s*`), "")
      .replace(/;\s*$/, "")
  );
}

function toLessonId(courseId, idx) {
  return `${courseId}-fccr-${String(idx).padStart(2, "0")}`;
}

