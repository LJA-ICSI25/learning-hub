/**
 * Splits docs/SECURITY_CONCEPTS_WORKPLACE_CURRICULUM.md into per-lesson HTML and
 * refreshes the Security track in assets/learn-hub-courses.js.
 *
 * Run from repo root: `npm run build:security` (runs this script then `build-security-quizzes.mjs`).
 * Running this file alone regenerates lesson HTML only and drops quiz steps until you re-run the quiz script.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";
import { marked } from "marked";
import { enrichSecurityLessonHtml } from "./security-lesson-enrich.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const mdPath = path.join(root, "docs", "SECURITY_CONCEPTS_WORKPLACE_CURRICULUM.md");
const coursesPath = path.join(root, "assets", "learn-hub-courses.js");
const outMdPath = path.join(root, "assets", "learn-hub-security-md.js");

function escapeAttr(s) {
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/"/g, "&quot;");
}

function parseLessons(md) {
  const lines = md.split(/\r?\n/);
  const out = [];
  let unit = "Security curriculum";
  let i = 0;
  while (i < lines.length) {
    const line = lines[i];
    const lev = line.match(/^#\s+(.+)$/);
    if (lev && /^Level\s+/i.test(lev[1])) {
      unit = lev[1].trim();
      i++;
      continue;
    }
    const idm = line.match(/^<!--\s*id:\s*(lh-sec-l\d+-\d+)\s*-->\s*$/i);
    if (idm) {
      let j = i + 1;
      while (j < lines.length && lines[j].trim() === "") j++;
      const h3 = lines[j] && lines[j].match(/^###\s+(L\d+-\d+):\s*(.+)$/);
      if (h3) {
        const id = idm[1];
        const code = h3[1];
        const rest = h3[2].trim();
        const title = `${code}: ${rest}`;
        j++;
        const bodyStart = j;
        while (j < lines.length && !/^<!--\s*id:\s*lh-sec-l\d+-\d+\s*-->\s*$/i.test(lines[j])) {
          j++;
        }
        const body = lines.slice(bodyStart, j).join("\n").trimEnd();
        out.push({ id, unit, title, body });
        i = j;
        continue;
      }
    }
    i++;
  }
  return out;
}

function wrapLessonHtml({ id, unit, title }, innerHtml) {
  const banner =
    `<p class="lh-tg-source"><strong>${escapeAttr(title)}</strong> · <span class="lh-tg-chapter">${escapeAttr(unit)}</span> · Lesson <code>${escapeAttr(id)}</code> · Source: <code>docs/SECURITY_CONCEPTS_WORKPLACE_CURRICULUM.md</code></p>` +
    '<hr class="teach-hr lh-md-start"/>' +
    `<div class="lh-tg-root tech-prose lh-tg-markdown lh-chapter-markdown">${innerHtml}</div>`;
  return banner;
}

function writeSecurityMdJs(lessons) {
  const obj = {};
  for (const L of lessons) {
    const raw = marked.parse(L.body, { gfm: true, breaks: false, async: false });
    if (typeof raw !== "string") throw new Error("marked.parse must return string");
    const enriched = enrichSecurityLessonHtml(L);
    obj[L.id] = wrapLessonHtml(L, raw + enriched);
  }
  const json = JSON.stringify(obj, null, 0);
  const body = `/* Auto-generated by scripts/build-security-from-markdown.mjs — edit docs/SECURITY_CONCEPTS_WORKPLACE_CURRICULUM.md then re-run */\n` +
    `(function () {\n  var C = ${json};\n  window.LEARN_HUB_SECURITY_MD = window.LEARN_HUB_SECURITY_MD || {};\n  for (var k in C) {\n    if (Object.prototype.hasOwnProperty.call(C, k)) window.LEARN_HUB_SECURITY_MD[k] = C[k];\n  }\n})();\n`;
  fs.writeFileSync(outMdPath, body, "utf8");
}

function patchCourses(lessons) {
  const coursesRaw = fs.readFileSync(coursesPath, "utf8");
  const m = coursesRaw.match(/^window\.LEARN_HUB_COURSES = (\[[\s\S]*\]);?\s*$/);
  if (!m) throw new Error("Could not parse learn-hub-courses.js");
  const courses = JSON.parse(m[1]);
  const sec = courses.find((c) => c.id === "security");
  if (!sec) throw new Error("security course missing");

  const welcome = {
    unit: "Start here",
    id: "security-01",
    kind: "learn",
    title: "Welcome to Security",
    narrative:
      "<h2>Security+ prep track (workplace + exam depth)</h2>" +
      "<p>This path is built to support <strong>CompTIA Security+ (SY0-701)</strong>–style preparation: each lesson pairs <strong>short curriculum anchors</strong> (outcomes, workplace framing, lab ideas) with an <strong>expanded study pack</strong> in <strong>Notes</strong>—definitions, long-form explanation, exam psychology, traps, and a mini scenario.</p>" +
      "<p><strong>How to study:</strong> read the curriculum bullets first, then scroll through the Security+ block. Use the sidebar <strong>Quiz</strong> steps after each level and the <strong>Capstone</strong> sets to force retrieval practice. Re-read any <strong>key term</strong> you cannot explain aloud in under 20 seconds.</p>" +
      "<h3>What “passing thinking” looks like</h3><ul>" +
      "<li>You map scenarios to <strong>CIA</strong>, <strong>controls</strong>, and <strong>IR phase</strong> without hesitating.</li>" +
      "<li>You separate <strong>authentication vs authorization</strong>, <strong>hashing vs encryption</strong>, and <strong>policy vs technical control</strong>.</li>" +
      "<li>You pick mitigations that reduce <strong>realistic business loss</strong>, not buzzword soup.</li>" +
      "<li>You default to <strong>evidence-preserving</strong> actions in any IR-style stem.</li>" +
      "</ul>" +
      '<p class="msg info"><strong>Ethics:</strong> labs assume an isolated VM and systems you own or have <strong>written authorization</strong> to test.</p>' +
      '<p class="msg info">Layout matches Tech+: <strong>Notes</strong> hold the long reading; <strong>Check-in</strong> column holds quizzes.</p>',
  };

  sec.lessons = [
    welcome,
    ...lessons.map((L) => ({
      unit: L.unit,
      id: L.id,
      kind: "learn",
      title: L.title,
      narrative: "",
    })),
  ];

  fs.writeFileSync(coursesPath, `window.LEARN_HUB_COURSES = ${JSON.stringify(courses)};\n`, "utf8");
}

const md = fs.readFileSync(mdPath, "utf8");
const lessons = parseLessons(md);
if (lessons.length < 50) {
  console.warn("Expected many lessons; got", lessons.length);
}
writeSecurityMdJs(lessons);
patchCourses(lessons);
const quiz = spawnSync(process.execPath, [path.join(__dirname, "build-security-quizzes.mjs")], {
  cwd: root,
  stdio: "inherit",
});
if (quiz.status !== 0) throw new Error("build-security-quizzes.mjs failed");
console.log("Wrote", lessons.length, "security lessons →", path.relative(root, outMdPath), "+ courses (with quizzes re-spliced)");
