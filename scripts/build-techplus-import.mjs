/**
 * Extracts teaching HTML from TechPlus_Study_Guide.html and writes learn-hub-techplus.js.
 * Run from repo: node Learn-Hub/scripts/build-techplus-import.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const learnHubRoot = path.resolve(__dirname, "..");
const guidePath = path.resolve(
  learnHubRoot,
  "..",
  "..",
  "TechPlus-Stuff",
  "TechPlus",
  "TechPlus_Study_Guide.html"
);
const outPath = path.join(learnHubRoot, "assets", "learn-hub-techplus.js");

function readGuide() {
  if (!fs.existsSync(guidePath)) {
    console.error("Missing:", guidePath);
    process.exit(1);
  }
  return fs.readFileSync(guidePath, "utf8");
}

/** First </section> closes chapter (no nested sections in source). */
function getSectionBlock(html, id) {
  const open = `<section id="${id}"`;
  const start = html.indexOf(open);
  if (start < 0) return null;
  const from = html.indexOf(">", start) + 1;
  const end = html.indexOf("</section>", from);
  if (end < 0) return null;
  return html.slice(from, end);
}

function chapterContentInner(sectionBlock) {
  const marker = '<div class="chapter-content">';
  const i = sectionBlock.indexOf(marker);
  if (i < 0) return null;
  let inner = sectionBlock.slice(i + marker.length);
  const stopMini = inner.indexOf('<div class="mini-quiz-mount"');
  const stopVocab = inner.indexOf('<div class="vocab-tools"');
  let cut = inner.length;
  if (stopMini >= 0) cut = Math.min(cut, stopMini);
  if (stopVocab >= 0) cut = Math.min(cut, stopVocab);
  return inner.slice(0, cut).trim();
}

function scrub(html) {
  return html
    .replace(/\sstyle="[^"]*"/g, "")
    .replace(/class="summary-box"/g, 'class="lh-tg-summary"')
    .replace(/class="example"/g, 'class="lh-tg-example"')
    .replace(/class="note"/g, 'class="lh-tg-note"')
    .replace(/class="key-concept"/g, 'class="lh-tg-key"')
    .replace(/class="wiley-essentials"/g, 'class="lh-tg-recap"')
    .replace(/class="wiley-head"/g, 'class="lh-tg-recap-head"');
}

function sliceBetween(html, startNeedle, endNeedle) {
  const a = html.indexOf(startNeedle);
  if (a < 0) return "";
  const b = endNeedle ? html.indexOf(endNeedle, a + startNeedle.length) : html.length;
  if (b < 0) return html.slice(a);
  return html.slice(a, b);
}

const PREFIX =
  '<p class="lh-tg-source lh-md-topic-source">Tech+ topic notes (bundled here). For quizzes, drills, and the acronym table, use the separate <strong>Tech+ Study Guide</strong> HTML from your materials if you have it.</p><hr class="teach-hr lh-md-start"/>';

function wrap(body) {
  return PREFIX + '<div class="lh-tg-root tech-prose lh-chapter-markdown">' + body + "</div>";
}

const html = readGuide();

const ch1 = scrub(chapterContentInner(getSectionBlock(html, "ch1") || ""));
const ch2 = scrub(chapterContentInner(getSectionBlock(html, "ch2") || ""));
const ch3 = scrub(chapterContentInner(getSectionBlock(html, "ch3") || ""));
const ch4 = scrub(chapterContentInner(getSectionBlock(html, "ch4") || ""));
const ch5 = scrub(chapterContentInner(getSectionBlock(html, "ch5") || ""));
const ch6 = scrub(chapterContentInner(getSectionBlock(html, "ch6") || ""));
const ch7 = scrub(chapterContentInner(getSectionBlock(html, "ch7") || ""));

const netSlice = scrub(
  sliceBetween(
    ch2,
    "<h2>Networking basics</h2>",
    "<h2>Displays, resolution, and projectors</h2>"
  )
);
const cloudSlice = scrub(
  sliceBetween(ch2, "<h2>Cloud &amp; virtualization</h2>", "<h2>Basic maintenance &amp; support mindset</h2>")
);
const raidSlice = scrub(sliceBetween(ch2, "<h2>RAID concepts (recognition level)</h2>", "<h2>IPv4 essentials for workstations</h2>"));
const backupSlice = scrub(
  sliceBetween(ch5, "<h2>Backups: scope and location</h2>", "<h2>Normalization (one-sentence version)</h2>")
);

const tech03Body =
  ch3 +
  '<h2>Programming &amp; data representation (study guide Ch.4)</h2>' +
  ch4;

const map = {
  "tech-01": wrap(ch1),
  "tech-02": wrap(ch2),
  "tech-03": wrap(tech03Body),
  "tech-04": wrap(ch5),
  "tech-05": wrap(ch6),
  "tech-06": wrap(netSlice + (ch7 ? '<h2>Acronym clusters (study guide Ch.7)</h2>' + ch7 : "")),
  "tech-07": wrap(cloudSlice),
  "tech-09": wrap(backupSlice),
  "tech-18": wrap(raidSlice),
};

const banner = `/* Auto-generated by scripts/build-techplus-import.mjs — do not edit by hand */\n`;
const json = JSON.stringify(map, null, 0);
const fileBody = `${banner}window.LEARN_HUB_TECHPLUS = ${json};\n`;
fs.writeFileSync(outPath, fileBody, "utf8");
