/**
 * Fixes lh-tg-source <strong> titles that reference the wrong book chapter (PDF bleed),
 * e.g. "Ch 1 — …" inside a Ch 4 lesson. Replaces with the first real heading in the
 * segment body, or the course lesson title as fallback.
 *
 * Run: node scripts/fix-techplus-source-banner-titles.mjs
 */
import fs from "fs";
import path from "path";
import vm from "vm";
import { fileURLToPath } from "url";
import {
  readMergedChapterMaps,
  writeChapterJsFiles,
  writeTechplusMdLoader,
} from "./techplus-chapter-io.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

const banner =
  "/* Auto-generated by scripts/build-techplus-from-markdown.mjs — edit chapters/*.md then re-run */\n" +
  "/* Patched: fix-techplus-source-banner-titles.mjs aligned lh-tg-source <strong> with segment body */\n";

function loadCourses() {
  const p = path.join(root, "assets", "learn-hub-courses.js");
  const t = fs.readFileSync(p, "utf8");
  const start = t.indexOf("[");
  const end = t.lastIndexOf("]");
  return JSON.parse(t.slice(start, end + 1));
}

function escapeHtml(s) {
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function decodeBasicEntities(s) {
  return String(s)
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&#39;/g, "'")
    .replace(/&quot;/g, '"');
}

/** Ch N from "Ch 4 — Operating Systems" */
function chapterNumFromSpan(text) {
  const m = String(text || "").trim().match(/^Ch\s*(\d{1,2})\b/i);
  return m ? parseInt(m[1], 10) : null;
}

/** Any "Ch N —" or "Ch N -" in banner strong (PDF chapter bleed into subsection title) */
function conflictingChapterInStrong(strongText, expectedCh) {
  const t = String(strongText || "");
  const re = /Ch\s*(\d{1,2})\s*[—\-–]/gi;
  let m;
  while ((m = re.exec(t))) {
    const k = parseInt(m[1], 10);
    if (k >= 1 && k <= 12 && k !== expectedCh) return k;
  }
  return null;
}

function firstHeadingFromBody(html) {
  const i = html.indexOf("lh-tg-root");
  if (i < 0) return "";
  const slice = html.slice(i);
  const h3 = slice.match(/<h3[^>]*>([^<]*)<\/h3>/i);
  if (h3) return decodeBasicEntities(h3[1]).replace(/\s+/g, " ").trim();
  const h2 = slice.match(/<h2[^>]*>([^<]*)<\/h2>/i);
  if (h2) return decodeBasicEntities(h2[1]).replace(/\s+/g, " ").trim();
  const h1 = slice.match(/<h1[^>]*>([^<]*)<\/h1>/i);
  if (h1) return decodeBasicEntities(h1[1]).replace(/\s+/g, " ").trim();
  const ex = slice.match(/<p>\s*EXERCISE\s+([\d.]+)\s*<\/p>\s*<p>([^<]+)<\/p>/i);
  if (ex) return `Exercise ${ex[1]} — ${decodeBasicEntities(ex[2]).trim()}`;
  return "";
}

function patchSourceStrong(html, newTitle) {
  if (!newTitle) return html;
  const esc = escapeHtml(newTitle);
  return html.replace(/(<p class="lh-tg-source"><strong>)([^<]*)(<\/strong>)/, `$1${esc}$3`);
}

function main() {
  const courses = loadCourses();
  const tech = courses.find((c) => c.id === "tech");
  const titleById = new Map((tech.lessons || []).map((L) => [L.id, L.title || ""]));

  const map = readMergedChapterMaps(root);
  let fixed = 0;

  for (const id of Object.keys(map)) {
    if (!/^tech-sg-\d{2}-\d{2}$/.test(id)) continue;
    let html = map[id];
    const spanM = html.match(/<span class="lh-tg-chapter">([^<]*)<\/span>/);
    const expectedCh = chapterNumFromSpan(spanM ? spanM[1] : "");
    const strongM = html.match(/<p class="lh-tg-source">[\s\S]*?<strong>([^<]*)<\/strong>/);
    if (!strongM || !expectedCh) continue;

    const strongText = strongM[1];
    const mismatchCh = conflictingChapterInStrong(strongText, expectedCh);
    if (mismatchCh === null) continue;

    let next =
      firstHeadingFromBody(html) ||
      decodeBasicEntities(String(titleById.get(id) || "")).trim() ||
      strongText;

    if (next === strongText) continue;

    const nextHtml = patchSourceStrong(html, next);
    if (nextHtml === html) continue;
    map[id] = nextHtml;
    fixed++;
  }

  const chHashes = writeChapterJsFiles(root, map, banner);
  writeTechplusMdLoader(root, chHashes, banner);

  console.log("Fixed", fixed, "segments where banner <strong> referenced a different chapter than lh-tg-chapter.");
}

main();
