/**
 * Parses docs/KALI_HANDS_ON_LAB_CURRICULUM.md for <!-- id: labs-kali-... --> lesson blocks
 * (optional <!-- unit: ... --> on the following line), writes assets/learn-hub-kali-md.js,
 * and patches the Labs course in assets/learn-hub-courses.js.
 *
 * Run: npm run build:kali
 * Regenerate Markdown from templates: npm run emit:kali-slow
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { marked } from "marked";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const mdPath = path.join(root, "docs", "KALI_HANDS_ON_LAB_CURRICULUM.md");
const coursesPath = path.join(root, "assets", "learn-hub-courses.js");
const outMdPath = path.join(root, "assets", "learn-hub-kali-md.js");

function escapeAttr(s) {
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/"/g, "&quot;");
}

function parseLessons(md) {
  const lines = md.split(/\r?\n/);
  const out = [];
  let currentUnit = "Kali hands-on labs";
  let i = 0;
  while (i < lines.length) {
    const idm = lines[i].match(/^<!--\s*id:\s*(labs-kali-[a-z0-9-]+)\s*-->\s*$/i);
    if (idm) {
      const id = idm[1];
      i++;
      const unitLine = lines[i] && lines[i].match(/^<!--\s*unit:\s*(.+?)\s*-->\s*$/i);
      if (unitLine) {
        currentUnit = unitLine[1].trim();
        i++;
      }
      while (i < lines.length && lines[i].trim() === "") i++;
      const bodyStart = i;
      while (i < lines.length && !/^<!--\s*id:\s*labs-kali-[a-z0-9-]+\s*-->\s*$/i.test(lines[i])) {
        i++;
      }
      const body = lines.slice(bodyStart, i).join("\n").trimEnd();
      let title = id;
      const m3 = body.match(/^###\s+(.+)$/m);
      const m2 = body.match(/^##\s+(.+)$/m);
      const m1 = body.match(/^#\s+(.+)$/m);
      if (m3) title = m3[1].trim();
      else if (m2) title = m2[1].trim();
      else if (m1) title = m1[1].trim();
      out.push({ id, unit: currentUnit, title, body });
      continue;
    }
    i++;
  }
  return out;
}

function wrapLessonHtml({ id, unit, title }, innerHtml) {
  const banner =
    `<p class="lh-tg-source"><strong>${escapeAttr(title)}</strong> · <span class="lh-tg-chapter">${escapeAttr(unit)}</span> · Lesson <code>${escapeAttr(id)}</code> · Source: <code>docs/KALI_HANDS_ON_LAB_CURRICULUM.md</code></p>` +
    '<hr class="teach-hr lh-md-start"/>' +
    `<div class="lh-tg-root tech-prose lh-tg-markdown lh-chapter-markdown">${innerHtml}</div>`;
  return banner;
}

function mdToLesson({ id, unit, title, body }) {
  const raw = marked.parse(body, { gfm: true, breaks: false, async: false });
  if (typeof raw !== "string") throw new Error("marked.parse must return string");
  return wrapLessonHtml({ id, unit, title }, raw);
}

function writeKaliMdJs(lessonMap) {
  const json = JSON.stringify(lessonMap, null, 0);
  const body =
    `/* Auto-generated by scripts/build-kali-from-markdown.mjs — edit docs/KALI_HANDS_ON_LAB_CURRICULUM.md (or npm run emit:kali-slow) then re-run */\n` +
    `(function () {\n  var C = ${json};\n  window.LEARN_HUB_KALI_MD = window.LEARN_HUB_KALI_MD || {};\n  for (var k in C) {\n    if (Object.prototype.hasOwnProperty.call(C, k)) window.LEARN_HUB_KALI_MD[k] = C[k];\n  }\n})();\n`;
  fs.writeFileSync(outMdPath, body, "utf8");
}

function patchLabsCourse(lessonDefs) {
  const coursesRaw = fs.readFileSync(coursesPath, "utf8");
  const m = coursesRaw.match(/^window\.LEARN_HUB_COURSES = (\[[\s\S]*\]);?\s*$/);
  if (!m) throw new Error("Could not parse learn-hub-courses.js");
  const courses = JSON.parse(m[1]);
  const labs = courses.find((c) => c.id === "labs");
  if (!labs) throw new Error("labs course missing");

  const welcome = {
    unit: "Start here",
    id: "labs-01",
    kind: "learn",
    title: "Welcome to Labs",
    narrative:
      "<p>Use this track for <strong>hands-on labs</strong> that sit outside the day-to-day HTML/CSS/JS/Python/SQL editors.</p>" +
      "<p>The <strong>Kali Linux Hands-On Lab Curriculum</strong> is intentionally <strong>slow</strong>: <strong>one sidebar lesson ≈ one lab session</strong> (often 45–120+ minutes). Read <strong>Background</strong> and <strong>Commands explained</strong> before typing. Keep a lab notebook as you go.</p>" +
      '<p class="msg info">Source: <code>docs/KALI_HANDS_ON_LAB_CURRICULUM.md</code>. Regenerate long-form Markdown from embedded templates: <code>npm run emit:kali-slow</code>. Rebuild Learn-Hub segments: <code>npm run build:kali</code>.</p>',
  };

  labs.lessons = [
    welcome,
    ...lessonDefs.map((d) => ({
      unit: d.unit,
      id: d.id,
      kind: "learn",
      title: d.title,
      narrative: "",
    })),
  ];

  const out = "window.LEARN_HUB_COURSES = " + JSON.stringify(courses) + ";\n";
  fs.writeFileSync(coursesPath, out, "utf8");
}

function main() {
  const md = fs.readFileSync(mdPath, "utf8");
  const parsed = parseLessons(md);
  if (parsed.length === 0) {
    throw new Error("No <!-- id: labs-kali-... --> lessons found in " + mdPath);
  }

  const lessonMap = {};
  const lessonDefs = [];
  for (const L of parsed) {
    lessonMap[L.id] = mdToLesson(L);
    lessonDefs.push({ unit: L.unit, id: L.id, title: L.title });
  }

  writeKaliMdJs(lessonMap);
  patchLabsCourse(lessonDefs);
  console.log("Lessons:", parsed.length, "→", path.relative(root, outMdPath), "+ Labs course");
}

main();
